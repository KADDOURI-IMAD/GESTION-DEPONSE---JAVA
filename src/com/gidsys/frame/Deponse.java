    private void ajouterBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_ajouterBtnActionPerformed
           try {
        String depNumber = depNombertxt.getText();
        String depType = depNombertxt1.getSelectedItem().toString();
        
        if (depNumber.isEmpty() || depType.isEmpty()) {
            JOptionPane.showMessageDialog(null, "Please fill in all the fields");
            return; // Exit the method if any field is empty
        }
        
        String requete = "insert into deponse(nomber, type) values (?, ?)";
        ps = conn.prepareStatement(requete);
        ps.setString(1, depNumber);
        ps.setString(2, depType);
        ps.execute();

        JOptionPane.showMessageDialog(null, "La dÃ©ponse est bien crÃ©Ã©e!");
        cleardata();
    } catch (Exception e) {
        System.out.println("--> SQLException : " + e);
    } finally {
        try {
            ps.close();
            rs.close();
        } catch (Exception e) {
             StringWriter sw = new StringWriter();
            PrintWriter pw = new PrintWriter(sw);
            e.printStackTrace(pw);
            String stackTrace = sw.toString();

            JOptionPane.showMessageDialog(null, stackTrace, "Error", JOptionPane.ERROR_MESSAGE);
        }
    } 
    affichage();
    }//GEN-LAST:event_ajouterBtnActionPerformed

