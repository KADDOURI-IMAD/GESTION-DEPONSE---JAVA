     public void deplace(){
        try{
            int row = logintable.getSelectedRow();
            this.test = (logintable.getModel().getValueAt(row,0).toString());
            String requets = "select  * from logintable where cin = '"+test+"'";
            ps = conn.prepareStatement(requets);
            rs = ps.executeQuery();
            
            if(rs.next()){
                String t1 = rs.getString("cin");
                cintxt.setText(t1);
                slcttxt.setText(t1);
                String t2 = rs.getString("nom");
                nomtxt.setText(t2);
                String t3 = rs.getString("prenom");
                prenomtxt.setText(t3);
                String t4 = rs.getString("login");
                logintxt.setText(t4);
                String t5 = rs.getString("password");
                passtxt.setText(t5);
                String t6 = rs.getString("sexe");
                String vbn = t6;
                if(vbn.equals("Masculin")){
                    jRadioButton1.setSelected(true);
                }else if(vbn.equals("FÃ©minin")){
                    jRadioButton2.setSelected(true);
                }
                String t7 = rs.getString("type");
                String vbn2 = t7;
                if(vbn2.equals("Gouverneur")){
                    jRadioButton3.setSelected(true);
                }else if(vbn2.equals("Comptable")){
                    jRadioButton5.setSelected(true);
                }else if(vbn2.equals("Utilisateur")){
                    jRadioButton4.setSelected(true);
                }
            }
        }catch(Exception e){
            System.out.println(e);
        }
    }
     
